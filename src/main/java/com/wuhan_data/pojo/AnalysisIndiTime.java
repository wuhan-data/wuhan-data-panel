package com.wuhan_data.pojo;

public class AnalysisIndiTime {
	String indiCode; // 指标id
	String freqName; // 指标值时间频度
	String startTime; // 指标可取的开始时间
	String endTime; // 指标可取的结束时间

	public String getIndiCode() {
		return indiCode;
	}

	public String getFreqName() {
		return freqName;
	}

	public String getStartTime() {
		return startTime;
	}

	public String getEndTime() {
		return endTime;
	}

}
